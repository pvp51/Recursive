package assignment2;

public class TreeType {
	
	private int data;
    private TreeType left;
    private TreeType right;
	
	public int getData() {
		return data;
	}
	public void setData(int data) {
		this.data = data;
	}
	public TreeType getLeft() {
		return left;
	}
	public void setLeft(TreeType left) {
		this.left = left;
	}
	public TreeType getRight() {
		return right;
	}
	public void setRight(TreeType right) {
		this.right = right;
	}
	

}
